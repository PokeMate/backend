package uzh.ase.pokemate.dto;

public class RatingDto {
	private int name;
	private int image;
	private int rating;

	public int getName() {
		return name;
	}

	public void setName(int name) {
		this.name = name;
	}

	public int getImage() {
		return image;
	}

	public void setImage(int image) {
		this.image = image;
	}

	public int getRating() {
		return rating;
	}

	public void setRating(int rating) {
		this.rating = rating;
	}

}
